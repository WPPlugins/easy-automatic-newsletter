<h2>Help Manual</h2>
<p>The   Easy Automatic Newsletter Lite 2.7.2 reuses your blog posts, and tweets to constitute an email newsletter. It comes with a   widget to allow anyone to subscribe   to your newsletter. Depending on the frequency you select, blog posts   from the prior period (i.e. the past month) are used for a newsletter.</p>
<br />
<p>The Settings section is where all the customization is entered and found.</p>
<p> 1.    Click Settings from the Easy Automatic Newsletter Lite tab/menu</p>
<p> 2.    Choose the Cron type you want to use (Cronjob versus WP Cron) – Review the help bubble for info on which to choose and why</p>
<p> 3.    Select the Email Frequency</p>
<p> 4.    Select the time (24 hour)you want your newsletter to be sent and the preferred day of the week.</p>
<p> 5.    Select the blog categories you wish to pull content from</p>
<p> 6.    Enter the Blog Post Character Limit (requires a numeric value) </p>
<p> 7.    Enter the email address to send test email newsletters to</p>
<p> 8.    Choose the Command type applies only if the selected Cron type is &ldquo;Cronjob&rdquo;</p>
<br />
<p><strong>FOR   CRONJOB USERS</strong><br /><br />We provide 3 different commands for the cron since some   servers are choosy. Try the &ldquo;PHP command first. If that doesn&rsquo;t work,   you have two other command flavors   to try. Be sure not to add multiple cron entries or multiple   newsletters may be sent.</p>
<p> 9.    Click the Save changes button</p>
<p><strong>FOR CRONJOB USERS</strong><br /><br />Use the settings that appear in the grey box below the Save   changes button to enter into your cron utility found inside your cPanel   or Control Panel for your domain (Linux users only)</p>
<p> 10.    Scroll down to &ldquo;Other Settings&rdquo;</p>
<p> 11.    Enter the Logo Path of your newsletter logo</p>
<p> 12.    Enter the Sender&rsquo;s Name</p>
<p> 13.    Enter the Sender&rsquo;s Email address</p>
<p> 14.    Enter the Subject line for your newsletter</p>
<p> 15.    Enter the Privacy Policy</p>
<p> 16.    Click the Save Changes button</p>
<p> 17.    Scroll down to Social Media Integration</p>
<p> 18.    If you wish to link Tweets   into your email newsletter, place a check inside the box and   enter the Twitter account (do not include the @ symbol), and the number   of tweets to include (the maximum is 20)</p>
